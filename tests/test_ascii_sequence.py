from dial.ascii import ASCIIRenderer
from dial.diagram import Diagram

from .helpers import eqdia


def test_asciisequence_header():
    r = ASCIIRenderer(Diagram('''
        diagram: Foo
        version: 1.0
        sequence: Bar
    '''))
    assert eqdia(str(r.render()), '''
    .................
    . DIAGRAM: Foo  .
    . version: 1.0  .
    .               .
    . SEQUENCE: Bar .
    .               .
    .................
    ''')

    r = ASCIIRenderer(Diagram('''
        diagram: Foo
        version: 1.0
        sequence:
    '''))
    assert eqdia(str(r.render()), '''
    ................
    . DIAGRAM: Foo .
    . version: 1.0 .
    .              .
    ................
    ''')


def test_asciisequence_modules():
    r = ASCIIRenderer(Diagram('''
        diagram: Foo
        version: 1.0
        sequence:

        foo.title: Foo
        foo -> bar
        foo -> thud
    '''))
    assert eqdia(str(r.render()), '''
    .............................
    . DIAGRAM: Foo              .
    . version: 1.0              .
    .                           .
    . +-----+ +-----+ +-------+ .
    . | Foo | | bar | | thud  | .
    . +-----+ +-----+ +-------+ .
    .............................
    ''')


