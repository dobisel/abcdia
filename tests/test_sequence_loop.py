from adia import Diagram

from .helpers import eqdia


def test_sequence_loop():
    d = Diagram('''
        diagram: Foo
        version: 1.0
        sequence:

        for: i in 0~10
          b -> a
          if: yes
            while: True
              a -> c
    ''')
    assert eqdia(d, '''
    ........................
    . DIAGRAM: Foo         .
    . version: 1.0         .
    .                      .
    . +---+ +---+    +---+ .
    . | b | | a |    | c | .
    . +---+ +---+    +---+ .
    .   |     |        |   .
    . ******************** .
    . * for i in 0~10    * .
    . ******************** .
    .   |     |        |   .
    .   |~~~~>|        |   .
    .   |     |        |   .
    .   |<----|        |   .
    .   |     |        |   .
    .   |   ************** .
    .   |   * if yes     * .
    .   |   ************** .
    .   |   ************** .
    .   |   * while True * .
    .   |   ************** .
    .   |     |        |   .
    .   |     |~~~~~~~>|   .
    .   |     |        |   .
    .   |     |<-------|   .
    .   |     |        |   .
    .   |   ************** .
    .   |   * end while  * .
    .   |   ************** .
    .   |   ************** .
    .   |   * end if     * .
    .   |   ************** .
    . ******************** .
    . * end for          * .
    . ******************** .
    .   |     |        |   .
    . +---+ +---+    +---+ .
    . | b | | a |    | c | .
    . +---+ +---+    +---+ .
    ........................
    ''')


def test_sequence_loop_isolation():
    d = Diagram('''
        diagram: Foo
        version: 1.0
        sequence:

        foo -> bar:
        for: i in range(10)
         a -> b: hello()

    ''')
    assert eqdia(d, '''
    ..........................................
    . DIAGRAM: Foo                           .
    . version: 1.0                           .
    .                                        .
    . +-----+ +-----+ +---+            +---+ .
    . | foo | | bar | | a |            | b | .
    . +-----+ +-----+ +---+            +---+ .
    .    |       |      |                |   .
    .    |~~~~~~>|      |                |   .
    .    |       |      |                |   .
    .    |<------|      |                |   .
    .    |       |      |                |   .
    .    |       |    ********************** .
    .    |       |    * for i in range(10) * .
    .    |       |    ********************** .
    .    |       |      |                |   .
    .    |       |      |~~~hello()~~~~~>|   .
    .    |       |      |                |   .
    .    |       |      |<---------------|   .
    .    |       |      |                |   .
    .    |       |    ********************** .
    .    |       |    * end for            * .
    .    |       |    ********************** .
    .    |       |      |                |   .
    . +-----+ +-----+ +---+            +---+ .
    . | foo | | bar | | a |            | b | .
    . +-----+ +-----+ +---+            +---+ .
    ..........................................
    ''')
