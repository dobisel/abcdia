def test_asciisequence_emptyif():
    r = ASCIIRenderer(Diagram('''
        diagram: Foo
        version: 1.0
        sequence:

        if
    '''))
    assert eqdia(r.dumps(), '''
    ................
    . DIAGRAM: Foo .
    . version: 1.0 .
    .              .
    . **********   .
    . * if     *   .
    . **********   .
    . **********   .
    . * end if *   .
    . **********   .
    .              .
    ................
    ''')

    r = ASCIIRenderer(Diagram('''
        diagram: Foo
        version: 1.0
        sequence:

        if
        else
    '''))
    assert eqdia(r.dumps(), '''
    ................
    . DIAGRAM: Foo .
    . version: 1.0 .
    .              .
    . **********   .
    . * if     *   .
    . **********   .
    . **********   .
    . * else   *   .
    . **********   .
    . **********   .
    . * end if *   .
    . **********   .
    .              .
    ................
    ''')


